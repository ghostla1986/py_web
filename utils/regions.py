def get_provinces():
    """获取省份列表"""
    return sorted(REGIONS.keys())


def get_cities(province):
    """获取指定省份的城市列表"""
    p = REGIONS.get(province)
    if not p:
        return []
    return sorted(p["cities"].keys())


def get_districts(province, city):
    """获取指定城市的区列表"""
    p = REGIONS.get(province)
    if not p:
        return []
    c = p["cities"].get(city)
    if not c:
        return []
    return sorted(c["districts"].keys())


def get_streets(province, city, district):
    """获取指定区的街道列表"""
    p = REGIONS.get(province)
    if not p:
        return []
    c = p["cities"].get(city)
    if not c:
        return []
    d = c["districts"].get(district)
    if not d:
        return []
    return d
